$(document).ready(function(){
	

	$("#link").click(function() {$(".list-item").show();
	});

	
	$(".submit-button").click(function() {$(".list-item").hide();
	});

	
	$(".cancel-button").click(function() {$(".list-item").hide();
	});
	
	
	$("#modal-overlay").click(function() {$(".list-item").hide();
	});

	});
	